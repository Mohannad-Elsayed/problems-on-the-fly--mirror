void solve() {
    int n; cin >> n; 
    deque<int> v(n), tv;
    getv(v);
    sort(all(v));
    tv = v;
    int ans1 = 0, ans2 = 0;
    while((v.front()+v.back()) % 2)
        v.pop_back(), ans1++;
    while((tv.front()+tv.back()) % 2)
        tv.pop_front(), ans2++;
    cout << min(ans1, ans2);
}